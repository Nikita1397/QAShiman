# Пользователь вводит с клавиатуры строку и символ
# для поиска. Посчитайте сколько раз в строке встречается
# искомый символ. Полученное число выведите на экран.

string = input("Введите строку:")
ch = input("Введите букву для проверки:")

def check(string, ch):
    if not string:
        return 0
    elif string[0] == ch:
        return 1 + check(string[1:], ch)
    else:
        return check(string[1:], ch)
print("Количество повторений буквы :")
print(check(string, ch))
print("Повторяется введенная буква ")
