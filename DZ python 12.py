# Пользователь вводит с клавиатуры строку. Посчитайте количество букв, цифр в строке
s =input('Введите слово ')
count_letter = 0
count_figure = 0
i = 0
while i < len(s):
    # Подсчет цифр
    if s[i].isdigit():
        count_figure += 1
    # Подсчет букв
    if s[i].islower():
        count_letter += 1
    i += 1
print(count_figure, ("Цифр"), count_letter, ("букв"))
# Вывод колличества букв и цифр в строке

# Пользователь вводит с клавиатуры строку. Проверьте, является ли введенная строка палиндромом.
